// heap usage in javascript
// Heap is a special tree-based data structure that satisfies the heap property.
// In a max heap, for any given node I, the value of I is greater than or equal to the values of its children.
// In a min heap, the value of I is less than or equal to the values of its children.
// Heaps are commonly used to implement priority queues, where the highest (or lowest) priority element is always at the root.

